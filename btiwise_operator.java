package operator;

public class btiwise_operator {

	public static void main(String[] args) {
	    int a=4;
	    int b=5;
	    int c=a&b;
	    System.out.println(c);
	  
	    int f=9;
	    int e=8;
	    int d=e|f;
	    System.out.println(d);
	    
	    int g=1;
	    int h=6;
	    int m= g^h;
	    System.out.println(m);
	    
	    int fai=10;
	    int sal=~fai;
	    System.out.println(sal);
	}

}